function extendData() {
  const raw = JSON.parse(readFileSync(DATA_PATH, 'utf-8'))

  let totalAdded = 0

  for (const category of raw.categories) {
    const additions = newQuestions[category.name]
    if (!additions) continue

    const existingNorm = new Set(category.questions.map(normalize))
    const toAdd = additions.filter((q) => !existingNorm.has(normalize(q)))

    category.questions.push(...toAdd)
    totalAdded += toAdd.length
    console.log(`${category.name}: +${toAdd.length} (${category.questions.length} total)`)
  }

  writeFileSync(DATA_PATH, JSON.stringify(raw, null, 2))
  console.log(`\nDone. Added ${totalAdded} new questions.`)
}

extendData()
